// Parse up to SIZE rows of '#'/'.' into a centered SIZE×SIZE seed. Anything not
// '#' is dead. Short/narrow patterns are centered in the 24×24 field.
export function seedFromRows(rows: string[]): Uint8Array {
  const data = new Uint8Array(SIZE * SIZE);
  const h = Math.min(rows.length, SIZE);
  const r0 = Math.floor((SIZE - h) / 2);
  for (let r = 0; r < h; r++) {
    const line = rows[r];
    const w = Math.min(line.length, SIZE);
    const c0 = Math.floor((SIZE - w) / 2);
    for (let c = 0; c < w; c++) {
      if (line[c] === '#') data[(r0 + r) * SIZE + (c0 + c)] = 1;
    }
  }
  return data;
}

